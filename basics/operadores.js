// Operadores matematicos
// + - * / % **
// += -= *= /= %= **=

console.log(10+10);
console.log(10-10);
console.log(10/10);
console.log(10*10);
console.log(10%10);
console.log(10**10); //pontencia
let num = 15;
console.log(num +=5); //num = num+5
console.log(num -=5);
console.log(num *=5);
console.log(num /=5);
console.log(num %=5);
console.log(num **=5);

//operadores booleanos
